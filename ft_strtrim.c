#include "libft.h"

char	*ft_strtrim(char const *s)
{
	size_t	start;
	size_t	end;
	char	*tmp;

	if (s == NULL)
		return (NULL);
	start = 0;
	end = ft_strlen(s);
	while (ft_iswhitespace(s[start]))
		start++;
	while (ft_iswhitespace(s[end - 1]))
		end--;
	if (end < start)
		end = start;
	tmp = ft_strnew(end - start);
	if (tmp == NULL)
		return (NULL);
	return (ft_strncpy(tmp, s + start, end - start));
}
